SemanticRecord = Dict[str, Any]


def load_semantic_jsonl(path: str, warnings: List[str]) -> List[SemanticRecord]:
    p = _resolve(path)
    if not path or str(path).strip() == "":
        return []
    if not p.is_file():
        warnings.append(f"semantic_memory_in missing: {p}")
        return []
    out: List[SemanticRecord] = []
    with p.open("r", encoding="utf-8") as f:
        for line in f:
            s = line.strip()
            if not s:
                continue
            try:
                rec = json.loads(s)
                if isinstance(rec, dict):
                    out.append(rec)
            except Exception:
                continue
    return out


def write_semantic_jsonl(path: Path, recs: Sequence[SemanticRecord]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        for r in recs:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")


def record_weight(rec: SemanticRecord) -> float:
    w = safe_float(rec.get("weight", 0.0))
    c = safe_float(rec.get("confidence", 1.0))
    if not math.isfinite(w):
        w = 0.0
    if not math.isfinite(c):
        c = 1.0
    return float(w * c)


def rule_applies_dim(rec: SemanticRecord, dim: int) -> bool:
    d = rec.get("dim", "all")
    if d in ("all", "*", None):
        return True
    try:
        return int(d) == int(dim)
    except Exception:
        return False


def motif_features(word: List[Tuple[int, int]], dim: int) -> Dict[str, Any]:
    gens = [int(i) for (i, _p) in word]
    pwrs = [int(p) for (_i, p) in word]
    uniq = len(set(gens)) if gens else 0
    repeats = sum(1 for j in range(1, len(word)) if word[j][0] == word[j - 1][0])
    inv_adj = sum(1 for j in range(1, len(word)) if word[j][0] == word[j - 1][0] and word[j][1] == -word[j - 1][1])
    mid = 0.5 * (dim - 1)
    mean_dist_mid = float(np.mean([abs(g - mid) for g in gens])) if gens else float("inf")
    power_l1 = float(np.sum(np.abs(pwrs))) if pwrs else 0.0
    return {
        "len": int(len(word)),
        "uniq_gens": int(uniq),
        "adj_repeats": int(repeats),
        "adj_inverse": int(inv_adj),
        "mean_dist_mid": float(mean_dist_mid),
        "power_l1": float(power_l1),
    }


def semantic_score(word: List[Tuple[int, int]], dim: int, memory: Sequence[SemanticRecord]) -> Tuple[float, float, List[str], List[str]]:
    """
    Returns (bonus, penalty, matched_bonus_motifs, matched_penalty_motifs)
    Matching is via rec['motif'] and simple deterministic feature predicates.
    """
    feat = motif_features(word, dim)
    bonus = 0.0
    penalty = 0.0
    mb: List[str] = []
    mp: List[str] = []
    for rec in memory:
        if not isinstance(rec, dict):
            continue
        if not rule_applies_dim(rec, dim):
            continue
        typ = str(rec.get("type", "")).strip().lower()
        motif = str(rec.get("motif", "")).strip()
        w = record_weight(rec)
        if w == 0.0:
            continue

        match = False
        # deterministic motif matching
        if motif == "avoid_long_words":
            match = feat["len"] >= 24
        elif motif == "prefer_short_words":
            match = feat["len"] <= 10
        elif motif == "avoid_adj_repeats":
            match = feat["adj_repeats"] >= 3
        elif motif == "avoid_inverse_cancellation":
            match = feat["adj_inverse"] >= 1
        elif motif == "prefer_generator_diversity":
            match = feat["uniq_gens"] >= max(3, int(0.15 * (dim - 1)))
        elif motif == "prefer_mid_generators":
            match = feat["mean_dist_mid"] <= 0.25 * (dim - 1)
        elif motif == "empty_support_artifact":
            # this is a post-hoc artifact motif; in generation we treat as avoid if word tends to concentrate too narrowly
            match = feat["uniq_gens"] <= 2
        elif motif == "poisson_like_warning":
            match = True  # global caution; applied as a small generic penalty
        elif motif == "active_argument_first":
            match = feat["uniq_gens"] >= 4 and feat["adj_repeats"] == 0
        else:
            match = False

        if not match:
            continue

        if typ == "prefer":
            bonus += float(abs(w))
            mb.append(motif)
        elif typ in ("avoid", "artifact", "caution"):
            penalty += float(abs(w))
            mp.append(motif)
    return float(bonus), float(penalty), mb, mp


def generate_semantic_from_diagnostics(
    *,
    dim: int,
    metrics: Dict[str, Any],
    source: str,
    now_iter: int,
) -> List[SemanticRecord]:
    """
    Deterministic templates, no LLM.
    """
    out: List[SemanticRecord] = []

    def rec(typ: str, motif: str, text: str, weight: float, conf: float, scope: str) -> SemanticRecord:
        return {
            "type": typ,
            "text": text,
            "motif": motif,
            "weight": float(weight),
            "confidence": float(conf),
            "source": str(source),
            "dim": int(dim),
            "scope": str(scope),
            "iter": int(now_iter),
        }

    sup = safe_float(metrics.get("support_overlap", float("nan")))
    pois = safe_float(metrics.get("poisson_like_fraction", float("nan")))
    arg = safe_float(metrics.get("active_argument_error", float("nan")))
    res = safe_float(metrics.get("residue_error", float("nan")))
    nv = safe_float(metrics.get("nv_error", float("nan")))

    if math.isfinite(sup) and sup < 0.25:
        out.append(
            rec(
                "avoid",
                "empty_support_artifact",
                "Avoid candidates that likely cause support mismatch (very low active-window overlap).",
                0.9,
                0.9,
                "gate",
            )
        )
    if math.isfinite(pois) and pois >= 1.0:
        out.append(
            rec(
                "caution",
                "poisson_like_warning",
                "Caution: Poisson-like behavior dominates; prioritize anti-Poisson and level repulsion improvements before residue tuning.",
                0.6,
                0.8,
                "search",
            )
        )
    if math.isfinite(arg) and arg <= 0.35 and (not math.isfinite(pois) or pois >= 0.5):
        out.append(
            rec(
                "prefer",
                "active_argument_first",
                "Prefer candidates that improve active-window argument counts before residue optimization.",
                0.7,
                0.8,
                "search",
            )
        )
    if math.isfinite(nv) and nv <= 1.0 and (not math.isfinite(pois) or pois <= 0.5):
        out.append(
            rec(
                "prefer",
                "prefer_generator_diversity",
                "Prefer generator-diverse words when anti-Poisson / rigidity improves.",
                0.7,
                0.75,
                "search",
            )
        )
    if math.isfinite(res) and res < 0.25 and (not math.isfinite(sup) or sup < 0.5):
        out.append(
            rec(
                "artifact",
                "empty_support_artifact",
                "Avoid candidates whose residue looks good but support overlap is inadequate (likely empty-window artifact).",
                0.95,
                0.9,
                "gate",
            )
        )
    return out


