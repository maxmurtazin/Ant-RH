@dataclass(frozen=True)
class EvalMetrics:
    J: float
    reward: float
    stable_ok: bool
    support_overlap: float
    active_argument_error: float
    nv_error: float
    poisson_like_fraction: float
    residue_error: float
    trace_error: float
    semantic_bonus: float
    semantic_penalty: float
    semantic_L: float
    word_len: int


def build_stabilized_operator(dim: int, word: List[Tuple[int, int]], *, target_radius: float) -> Tuple[np.ndarray, Dict[str, Any]]:
    eps = 1e-12
    theta_base = math.pi / 8.0
    n = int(dim)
    H = np.zeros((n, n), dtype=np.complex128)
    for k, (gi, p) in enumerate(word):
        A = make_stable_generator(n, int(gi), int(p), theta_base=theta_base)
        ck = 1.0 / math.sqrt(float(k) + 1.0)
        H = H + complex(ck) * A
    H = hermitian_part(H)
    H = remove_trace(H)
    H, _ = normalize_spectral_radius(H, target_radius=target_radius, eps=eps)
    rep = {"target_radius": float(target_radius)}
    return H, rep


def evaluate_word(
    *,
    dim: int,
    word: List[Tuple[int, int]],
    target_levels: np.ndarray,
    L_grid: np.ndarray,
    windows: List[Tuple[float, float]],
    eta: float,
    n_contour_points: int,
    support_overlap_min: float,
    anti_poisson_margin: float,
    residue_margin: float,
    trace_margin: float,
    semantic_L: float,
    null_penalty_weight: float,
    complexity_penalty_weight: float,
    seed: int,
) -> Tuple[EvalMetrics, Dict[str, Any], Optional[np.ndarray]]:
    eps = 1e-12
    target_radius = float(max(4.0, dim / 4.0))
    H, rep = build_stabilized_operator(dim, word, target_radius=target_radius)
    w = safe_eigvalsh(H, seed=seed)
    if w is None or w.size < 8:
        J = 1e6 + float(semantic_L)
        return (
            EvalMetrics(J, 1e-9, False, 0.0, 1.0, 10.0, 1.0, 1.0, 1.0, 0.0, 0.0, semantic_L, len(word)),
            rep,
            None,
        )
    levels = unfold_to_mean_spacing_one(w)
    # affine align to target scale
    tgt = np.asarray(target_levels, dtype=np.float64).reshape(-1)
    tgt = tgt[np.isfinite(tgt)]
    try:
        oq = np.quantile(levels, [0.1, 0.9])
        tq = np.quantile(tgt, [0.1, 0.9])
        a = float(max(1e-12, tq[1] - tq[0]) / max(1e-12, oq[1] - oq[0]))
        b = float(tq[0] - a * oq[0])
        levels = (a * levels + b).astype(np.float64, copy=False)
    except Exception:
        pass

    # Stage B: support/argument
    n_active = 0
    n_both = 0
    arg_errs = []
    for (a0, b0) in windows:
        n_op = rd.count_in_window(levels, float(a0), float(b0))
        n_tg = rd.count_in_window(tgt, float(a0), float(b0))
        if (n_op == 0) and (n_tg == 0):
            continue
        n_active += 1
        if (n_op > 0) and (n_tg > 0):
            n_both += 1
        arg_errs.append(abs(int(n_op) - int(n_tg)) / float(max(1, int(n_tg))))
    support_overlap = float(n_both) / float(max(1, n_active))
    active_arg = float(np.median(np.asarray(arg_errs, dtype=np.float64))) if arg_errs else 1.0

    # Stage C: NV/anti-Poisson
    op_nv = number_variance_curve(levels, L_grid)
    tg_nv = number_variance_curve(tgt, L_grid)
    nv_err = float(curve_l2(op_nv, tg_nv))
    dP = float(curve_l2(op_nv, sigma2_poisson(L_grid)))
    dG = float(curve_l2(op_nv, sigma2_gue_asymptotic(L_grid)))
    poisson_like = bool(math.isfinite(dP) and math.isfinite(dG) and dP < dG)
    poisson_like_fraction = 1.0 if poisson_like else 0.0
    anti_poisson_pen = float(max(0.0, (dG - dP) - float(anti_poisson_margin))) if (math.isfinite(dP) and math.isfinite(dG)) else 1.0

    # Stage D: residue/trace
    res_errs = []
    tr_errs = []
    sigmas = [0.5, 1.0, 2.0, 4.0]
    for (a0, b0) in windows:
        n_op = rd.count_in_window(levels, float(a0), float(b0))
        n_tg = rd.count_in_window(tgt, float(a0), float(b0))
        if (n_op == 0) and (n_tg == 0):
            continue
        I_op = rd.residue_proxy_count(levels, float(a0), float(b0), eta=float(eta), n_contour_points=int(n_contour_points))
        I_tg = rd.residue_proxy_count(tgt, float(a0), float(b0), eta=float(eta), n_contour_points=int(n_contour_points))
        err = abs(float(I_op.real) - float(I_tg.real)) / max(1.0, abs(float(I_tg.real)))
        res_errs.append(float(err) + 0.10 * float(abs(I_op.imag)))
        c = 0.5 * (float(a0) + float(b0))
        for s in sigmas:
            Sop = rd.trace_formula_proxy(levels, center=float(c), sigma=float(s))
            Stg = rd.trace_formula_proxy(tgt, center=float(c), sigma=float(s))
            tr_err = abs(math.log1p(max(0.0, float(Sop))) - math.log1p(max(0.0, float(Stg)))) if (math.isfinite(Sop) and math.isfinite(Stg)) else float("nan")
            if math.isfinite(tr_err):
                tr_errs.append(float(tr_err))
    residue_err = float(np.median(np.asarray(res_errs, dtype=np.float64))) if res_errs else 1.0
    trace_err = float(np.median(np.asarray(tr_errs, dtype=np.float64))) if tr_errs else 1.0

    # Stage E: null penalty (shuffled/reversed target)
    rng2 = np.random.default_rng(int(seed) + 12345)
    shuffled = tgt.copy()
    rng2.shuffle(shuffled)
    reversed_t = tgt[::-1].copy()
    def core_vs(t2: np.ndarray) -> float:
        nv2 = float(curve_l2(op_nv, number_variance_curve(t2, L_grid)))
        ae = []
        for (a0, b0) in windows:
            n_op = rd.count_in_window(levels, float(a0), float(b0))
            n_tg = rd.count_in_window(t2, float(a0), float(b0))
            if (n_op == 0) and (n_tg == 0):
                continue
            ae.append(abs(int(n_op) - int(n_tg)) / float(max(1, int(n_tg))))
        aem = float(np.median(np.asarray(ae, dtype=np.float64))) if ae else 1.0
        return float((nv2 if math.isfinite(nv2) else 10.0) + aem)
    null_best = min(core_vs(shuffled), core_vs(reversed_t))
    real_core = float((nv_err if math.isfinite(nv_err) else 10.0) + active_arg)
    L_null = float(max(0.0, real_core - float(null_best)))

    # Combine J (fixed weights; semantic & complexity are explicit)
    # Keep stable candidates prioritized; if support overlap fails, penalize hard.
    L_support = float(max(0.0, float(support_overlap_min) - support_overlap))
    J = (
        10.0 * float(L_support)
        + 3.0 * float(active_arg)
        + 2.0 * float(nv_err if math.isfinite(nv_err) else 10.0)
        + 3.0 * float(poisson_like_fraction)
        + 1.0 * float(max(0.0, residue_err - residue_margin))
        + 1.0 * float(max(0.0, trace_err - trace_margin))
        + float(null_penalty_weight) * float(L_null)
        + float(complexity_penalty_weight) * float(len(word) / max(1, 32))
        + 1.0 * float(semantic_L)
        + 0.5 * float(anti_poisson_pen)
    )
    J = float(min(max(J, 0.0), 1e6))
    reward = 1e-9 if support_overlap < support_overlap_min else float(1.0 / (1.0 + J))

    em = EvalMetrics(
        J=float(J),
        reward=float(reward),
        stable_ok=True,
        support_overlap=float(support_overlap),
        active_argument_error=float(active_arg),
        nv_error=float(nv_err),
        poisson_like_fraction=float(poisson_like_fraction),
        residue_error=float(residue_err),
        trace_error=float(trace_err),
        semantic_bonus=0.0,
        semantic_penalty=0.0,
        semantic_L=float(semantic_L),
        word_len=int(len(word)),
    )
    rep.update({"L_support": L_support, "L_null": L_null, "anti_poisson_pen": anti_poisson_pen})
    return em, rep, levels


